    public void setup(int index, String imagePath, boolean normalCollision, boolean raftingCollision) {
    	
        Utilities util = new Utilities();
        
        try {
        	
            tile[index] = new Tile(1);
            
            BufferedImage image = ImageIO.read(getClass().getResourceAsStream(imagePath + ".png"));
            
            tile[index].setFrame(0, util.scaleImage(image, gp.tileSize, gp.tileSize));
            
            tile[index].setNormalCollision(normalCollision);
            
            tile[index].setRaftingCollision(raftingCollision);
            
        } 
        catch (IOException e) {
        	
            gp.endGameThread();
            
            e.printStackTrace();
        }
    }

    public void setupAnimatedTile(int index, String imagePath, boolean normalCollision, boolean raftingCollision, int numFrames) {
       
    	Utilities util = new Utilities();
    	
        try {
        	
            tile[index] = new Tile(numFrames);
            
            for (int i = 0; i < numFrames; i++) {
            	
                BufferedImage image = ImageIO.read(getClass().getResourceAsStream(imagePath + i + ".png"));
                
                tile[index].setFrame(i, util.scaleImage(image, gp.tileSize, gp.tileSize));
            }
            
            tile[index].setNormalCollision(normalCollision);
            
            tile[index].setRaftingCollision(raftingCollision);
        } 
        catch (IOException e) {
        	
            gp.endGameThread();
            
            e.printStackTrace();
        }
    }


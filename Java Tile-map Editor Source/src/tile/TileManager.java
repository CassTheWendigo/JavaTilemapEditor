    public void getWorldSize() {
    	
        try {
        	
            InputStream is = getClass().getResourceAsStream("/maps/" + mapName + ".txt");
            
            BufferedReader br = new BufferedReader(new InputStreamReader(is));
            
            String currentLine = br.readLine();
            
            String[] columns = currentLine.split(" ");
            
            worldCols = columns.length;
            
            ArrayList<String> colData = new ArrayList<>();
            
            while ((currentLine = br.readLine()) != null) {
            	
                String[] rows = currentLine.split(" ");
                
                colData.add(rows[1]);
            }
            
            worldRows = colData.size();
            
            br.close();
            
        } 
        catch (Exception e) {
        	
            e.printStackTrace();
        }
    }

    public void loadMap() {
    	
        try {
        	
            InputStream is = getClass().getResourceAsStream("/maps/" + mapName + ".txt");
            
            BufferedReader br = new BufferedReader(new InputStreamReader(is));
            
            int col = 0, row = 0;
            
            while (col < worldCols && row < worldRows) {
            	
                String line = br.readLine();
                
                while (col < worldCols) {
                	
                    String[] numbers = line.split(" ");
                    
                    int num = Integer.parseInt(numbers[col]);
                    
                    mapTileNum[col][row] = num;
                    
                    col++;
                }
                
                if (col == worldCols) {
                	
                    col = 0;
                    
                    row++;
                }
            }
            
            br.close();
            
        } 
        catch (Exception e) {
        	
            e.printStackTrace();
        }
    }

    public void updateFrame() {
    	
        long currentTime = System.currentTimeMillis();
        
        if (currentTime - lastFrameTime >= frameDelay) {
        	
            currentFrame++;
            
            lastFrameTime = currentTime;
        }
    }

    public void draw(Graphics2D g2) {
    	
        updateFrame(); // Ensure frame updates globally

        int worldCol = 0, worldRow = 0;
        
        while (worldCol < worldCols && worldRow < worldRows) {
        	
            int tileNum = mapTileNum[worldCol][worldRow];
            
            int worldX = worldCol * gp.tileSize;
            
            int worldY = worldRow * gp.tileSize;
            
            int screenX = worldX - gp.player.worldX + gp.player.screenX;
            
            int screenY = worldY - gp.player.worldY + gp.player.screenY;
            
            if (worldX + gp.tileSize > gp.player.worldX - gp.player.screenX && worldX - gp.tileSize < gp.player.worldX + gp.player.screenX && worldY + gp.tileSize > gp.player.worldY - gp.player.screenY && worldY - gp.tileSize < gp.player.worldY + gp.player.screenY) {

                Tile currentTile = tile[tileNum];
                
                if (currentTile.isAnimated()) {
                	
                    // If the tile is animated, draw the current frame based on global frame index
                    int frameIndex = (currentFrame / currentTile.getTotalFrames()) % currentTile.getTotalFrames();
                    
                    g2.drawImage(currentTile.getFrame(frameIndex), screenX, screenY, gp.tileSize, gp.tileSize, null);
                } 
                else {
                	
                    // If the tile is not animated, draw the static image
                    g2.drawImage(currentTile.getFrame(0), screenX, screenY, gp.tileSize, gp.tileSize, null);
                }
            }
            
            worldCol++;
            
            if (worldCol == worldCols) {
            	
                worldCol = 0;
                
                worldRow++;
            }
        }
    }

    public void changeMap(int mapNumber) {
    	
        mapName = "map" + mapNumber;
        
        getWorldSize();
        
        loadMap();
    }
